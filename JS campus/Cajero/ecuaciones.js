function ecuacion1() {
    let cantidadTotal = parseInt(document.getElementById("cantidad").value);
    //esta constante va a tomar y transformar a numero que agregarmos en el input de cantidad
    let cantidadCuentaOne = parseInt(document.getElementById("cuentaOne").value);
    //obtenemos los datos de cuenta uno
    let cantidadCuentaTwo = parseInt(document.getElementById("cuentaTwo").value);
    //obtenemos los datos de cuenta dos
  
    //vamos a realizar los calculos
  
    document.getElementById("cuentaOne").value = cantidadCuentaOne - cantidadTotal;
  
    document.getElementById("cuentaTwo").value = cantidadCuentaTwo + cantidadTotal;
  }
  
